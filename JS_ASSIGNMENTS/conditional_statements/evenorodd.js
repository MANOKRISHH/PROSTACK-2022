//WAP to print even or odd for a given number

function printEvenOrOdd(num) {
    if (num % 2 === 0) {
      console.log(num + " is even");
    } else {
      console.log(num + " is odd");
    }
  }
  
  // Example usage:
  printEvenOrOdd(15);  // Output: "5 is odd"
  printEvenOrOdd(22); // Output: "10 is even"
  
