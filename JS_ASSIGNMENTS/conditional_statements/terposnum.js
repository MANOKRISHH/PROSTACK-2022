//WAP to check if a number is posiive or not using a ternary operator

// prompt the user to enter a number
var num = 24;

// convert the number from a string to an integer
num = parseInt(num);

// use a ternary operator to check if the number is positive
var result = (num > 0) ? "The number is positive" : "The number is not positive";

// output the result
console.log(result);
